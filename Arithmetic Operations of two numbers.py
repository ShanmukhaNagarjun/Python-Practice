# addition, subtraction, multiplication, division, modulus, exponentiation and floor division of the given two numbers

x = int(input("Enter first number: "))
y = int(input("Enter second number: "))
print("The value of", x, "+", y, "is: ", x+y)
print("The value of", x, "-", y, "is: ", x-y)
print("The value of", x, "*", y, "is: ", x*y)
print("The value of", x, "/", y, "is: ", x/y)
print("The value of", x, "%", y, "is: ", x%y)
print("The value of", x, "**", y, "is: ", x**y)
print("The value of", x, "//", y, "is: ", x//y)